#include<stdio.h>
#include<math.h>
int main()
{
	int a=20,b=10,add=0,sub=0,mul=0,div=0,modulo=0;
	add=a+b;
	sub=a-b;
	mul=a*b;
	div=a/b;
	modulo=a%b;
	printf("After Addition value is %d",add);
	printf("After Subtraction the value is %d",sub);
	printf("After multiplication the product is %d",mul);
	printf("After divsion value is %d",div);
	printf("After modulo Division value is %d",modulo);
	return 0;
}
